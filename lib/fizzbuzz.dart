class Fizzbuzz {
  String convert(int num) {
    var result = "";

    if (num > 0) {
      if (num % 3 == 0) {
        result += "Fizz";
      }

      if (num % 5 == 0) {
        result += "Buzz";
      }
    }

    if (result.isEmpty) {
      result = num.toString();
    }

    return result;
  }
}
